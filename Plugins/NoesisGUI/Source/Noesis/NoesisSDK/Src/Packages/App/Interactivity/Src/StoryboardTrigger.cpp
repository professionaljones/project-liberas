#include <NsApp/StoryboardTrigger.h>
#include <NsGui/Storyboard.h>
#include <NsGui/UIElementData.h>
#include <NsCore/ReflectionImplement.h>
#include <NsCore/TypeId.h>


using namespace Noesis;
using namespace NoesisApp;


////////////////////////////////////////////////////////////////////////////////////////////////////
StoryboardTrigger::StoryboardTrigger()
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////
StoryboardTrigger::~StoryboardTrigger()
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////
Storyboard* StoryboardTrigger::GetStoryboard() const
{
    return GetValue<Ptr<Storyboard>>(StoryboardProperty);
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void StoryboardTrigger::SetStoryboard(Storyboard* value)
{
    SetValue<Ptr<Storyboard>>(StoryboardProperty, value);
}

////////////////////////////////////////////////////////////////////////////////////////////////////
Ptr<StoryboardTrigger> StoryboardTrigger::Clone() const
{
    return StaticPtrCast<StoryboardTrigger>(Freezable::Clone());
}

////////////////////////////////////////////////////////////////////////////////////////////////////
Ptr<StoryboardTrigger> StoryboardTrigger::CloneCurrentValue() const
{
    return StaticPtrCast<StoryboardTrigger>(Freezable::CloneCurrentValue());
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void StoryboardTrigger::OnStoryboardChanged(const DependencyPropertyChangedEventArgs&)
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void StoryboardTrigger::OnStoryboardChanged(DependencyObject* d,
    const DependencyPropertyChangedEventArgs& e)
{
    StoryboardTrigger* trigger = (StoryboardTrigger*)d;
    trigger->OnStoryboardChanged(e);
}

////////////////////////////////////////////////////////////////////////////////////////////////////
NS_BEGIN_COLD_REGION

NS_IMPLEMENT_REFLECTION(StoryboardTrigger)
{
    NsMeta<TypeId>("NoesisApp.StoryboardTrigger");

    DependencyData* data = NsMeta<DependencyData>(TypeOf<SelfClass>());
    data->RegisterProperty<Ptr<Storyboard>>(StoryboardProperty, "Storyboard",
        PropertyMetadata::Create(Ptr<Storyboard>(), PropertyChangedCallback(OnStoryboardChanged)));
}

////////////////////////////////////////////////////////////////////////////////////////////////////
const DependencyProperty* StoryboardTrigger::StoryboardProperty;
