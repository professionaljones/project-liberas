#include <NsApp/StoryboardAction.h>
#include <NsGui/DependencyData.h>
#include <NsGui/Storyboard.h>
#include <NsCore/TypeId.h>
#include <NsCore/ReflectionImplement.h>


using namespace NoesisApp;
using namespace Noesis;
using namespace NoesisApp;


////////////////////////////////////////////////////////////////////////////////////////////////////
StoryboardAction::StoryboardAction()
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////
StoryboardAction::~StoryboardAction()
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////
Storyboard* StoryboardAction::GetStoryboard() const
{
    return GetValue<Ptr<Storyboard>>(StoryboardProperty);
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void StoryboardAction::SetStoryboard(Storyboard* storyboard)
{
    SetValue<Ptr<Storyboard>>(StoryboardProperty, storyboard);
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void StoryboardAction::OnStoryboardChanged(DependencyObject*,
    const DependencyPropertyChangedEventArgs&)
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////
NS_BEGIN_COLD_REGION

NS_IMPLEMENT_REFLECTION(StoryboardAction)
{
    NsMeta<TypeId>("NoesisApp.StoryboardAction");

    DependencyData* data = NsMeta<DependencyData>(TypeOf<SelfClass>());
    data->RegisterProperty<Ptr<Storyboard>>(StoryboardProperty, "Storyboard",
        PropertyMetadata::Create(Ptr<Storyboard>()));
}

////////////////////////////////////////////////////////////////////////////////////////////////////
const DependencyProperty* StoryboardAction::StoryboardProperty;
